import statistics as s

#harmonic_mean
print(s.harmonic_mean([1,5,10]))
#mean
print(s.mean([1,5,10]))
#median
print(s.median([1,5,10]))
#median_grouped

#median_high
print(s.median_high([1,5,10]))
#median_low
print(s.median_low([1,5,10,13]))
#mode
print(s.mode([1,5,10]))
#pstdev(Standard deviation of all population)
print(s.pstdev([1,5,10]))
#stdev(Standard deviation of a sample data)
print(s.stdev([1,5,10]))
#pvariance
print(s.pvariance([1,5,10]))
#variance
print(s.variance([1,5,10]))
