import math



a = 5
b = 10

print(math.factorial(a))

#acos - arc cosine (value between - 1 to 1)
print(math.acos(-0.2))

#cos

print(math.cos(0.92))

#sqrt

print(math.sqrt(9))
#ceil(round off number in upward)
print(math.ceil(7.17))

#comb

x = 3
y = 2
print(math.comb(3,2))

#copysign

print(math.copysign(-3,2))

#degrees

print(math.degrees(34.6))

#exponential

print(math.exp(3))

#fabs - absolute number

print(math.fabs(-0.243))

#floor(opposite to ceil - round the number downwards)

print(math.floor(-0.76))
#fmod
print(math.fmod(7,4))
#isclose
print(math.isclose(4.7,5,rel_tol = 1,abs_tol = 0.2))
#isfinite
print(math.isfinite(math.inf))
#isinf
print(math.isinf(43))
#isnan(Not a number)
print(math.isnan(0.7))

#pow
print(math.pow(5,4))
#prod
p = (2,3,4)
print(math.prod(p))
#radians
print(math.radians(20.45))

#remainder
print(math.remainder(7,4))
